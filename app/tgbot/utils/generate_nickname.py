import random

adjectives_male = [
    "Смешной", "Умный", "Смелый", "Творческий", "Забавный",
    "Тайный", "Скорый", "Мудрый", "Светлый", "Тёмный",
    "Позитивный", "Сильный", "Быстрый", "Легендарный", "Чудесный"
]

adjectives_female = [
    "Смешная", "Умная", "Смелая", "Творческая", "Забавная",
    "Тайная", "Скорая", "Мудрая", "Светлая", "Тёмная",
    "Позитивная", "Сильная", "Быстрая", "Легендарная", "Чудесная"
]

nouns_male = [
    "Лев", "Тигр", "Кот", "Сокол", "Дракон",
    "Орел", "Феникс", "Волк", "Медведь", "Слон"
]

nouns_female = [
    "Лисица", "Рысь", "Пантера", "Сова", "Белка",
    "Косуля", "Лань", "Кошка", "Тигрица", "Львица"
]


def generate_random_nickname():
    """
    Генерирует случайный никнейм, рандомно выбирая между мужским и женским вариантом.
    """
    if random.choice([True, False]):  # 50/50 шанс для каждого пола
        adjective = random.choice(adjectives_male)
        noun = random.choice(nouns_male)
    else:
        adjective = random.choice(adjectives_female)
        noun = random.choice(nouns_female)

    return f"{adjective} {noun}"